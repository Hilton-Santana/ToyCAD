#include "color.h"

//-------------------------------------------------------------------------
Color::Color()
: m_r(0.0), m_g(0.0), m_b(0.0)
{
}
//-------------------------------------------------------------------------

//-------------------------------------------------------------------------
Color::Color( double _r, double _g, double _b )
: m_r(_r), m_g(_g), m_b(_b)
{
}
//-------------------------------------------------------------------------

//-------------------------------------------------------------------------
Color::Color( vector<double> _rgb )
: m_r(_rgb[0]), m_g(_rgb[1]), m_b(_rgb[2])
{
}
//-------------------------------------------------------------------------

//-------------------------------------------------------------------------
vector<double> 
Color::getRGB()
{
 vector<double> tempRGB( 3 );
 tempRGB[0] = m_r;
 tempRGB[1] = m_g;
 tempRGB[2] = m_b;
 return tempRGB;
}
//-------------------------------------------------------------------------
