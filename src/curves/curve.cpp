#include "curve.h"

//-------------------------------------------------------------------------
Curve::Curve()
: m_nPts( 0 )
, m_nSdv( 1 )
, m_selected( false )
{
}
//-------------------------------------------------------------------------

//-------------------------------------------------------------------------
Curve::~Curve()
{
}
//-------------------------------------------------------------------------

//-------------------------------------------------------------------------
vector<Point> Curve::getSubdivPoints()
{
 vector<Point> sdvPts( m_nSdv+1 );
 for( int i = 0; i <= m_nSdv; i++ )
 {
  double t = (double)i / (double)m_nSdv;
  sdvPts[i] = getPoint( t );
 }
 return sdvPts;
}
