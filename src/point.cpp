#include "point.h"

//-------------------------------------------------------------------------
Point::Point()
: m_x(0.0), m_y(0.0)
{
}
//-------------------------------------------------------------------------

//-------------------------------------------------------------------------
Point::Point( double _cX, double _cY )
: m_x(_cX), m_y(_cY)
{
}
//-------------------------------------------------------------------------

//-------------------------------------------------------------------------
Point::Point( vector<double> _coords )
: m_x(_coords[0]), m_y(_coords[1])
{
}
//-------------------------------------------------------------------------

//-------------------------------------------------------------------------
vector<double> 
Point::getCoords()
{
 vector<double> tempCoords( 2 );
 tempCoords[0] = m_x;
 tempCoords[1] = m_y;
 return tempCoords;
}
//-------------------------------------------------------------------------
